#include "push_swap.h"

long long	ft_atoi(char *str)
{
	long long	res;
	int			min;
	int			i;

	min = 1;
	res = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			min = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if ((res * min) > 2147483647 || (res * min) < -2147483648)
		ft_error();
	return (res * min);
}
